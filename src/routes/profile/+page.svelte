<h3>サイトを運営しています</h3>

<ul>
<li><a href="https://gotoeveryone.k2ss.info/" target="_blank" rel="noopener">Go to Everyone!</a></li>
</ul>

2003年より運営している、世界中の囲碁ファンに向けたプロ棋戦の結果配信サイトです。
実はエンジニアとして学んだことを試す場でもあります。
私自身は中学生の時に「ヒカルの碁」で囲碁にハマり、現在の棋力はアマチュア五段ぐらいです。
主な利用者は海外の方なので全て英語です。

<ul>
<li><a href="https://waaru.k2ss.info/" target="_blank" rel="noopener">Waaru</a></li>
</ul>

